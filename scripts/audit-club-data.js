#!/usr/bin/env node
/**
 * Club data completeness audit.
 * Parses src/data/padel-courts.ts and reports which enrichment fields are
 * missing per club, prioritized by club size (court count) and rating volume.
 * Output: DATA_ENRICHMENT_REPORT.md
 */

const fs = require("fs");
const path = require("path");

const COURTS_FILE = path.join(__dirname, "..", "src", "data", "padel-courts.ts");
const OUT_FILE = path.join(__dirname, "..", "DATA_ENRICHMENT_REPORT.md");

const ts = fs.readFileSync(COURTS_FILE, "utf8");
const blocks = ts.split(/\n\s*\{\s*\n\s*id:\s*/).slice(1);

const FIELDS = [
  ["description", /description:\s*"[^"]{20,}/],
  ["numberOfCourts", /numberOfCourts:\s*[1-9]/],
  ["openingHours", /openingHours:\s*\{/],
  ["phone", /phone:\s*"[^"]{7,}/],
  ["website", /website:\s*"http/],
  ["pricingText", /pricingText:\s*"[^"]{4,}/],
  ["image (ogImageUrl)", /ogImageUrl:\s*"[^"]{10,}/],
  ["amenities", /amenities:\s*\[\s*"/],
  ["courtSurface", /courtSurface:\s*"[^"]{3,}/],
  ["instagram", /instagram:\s*"[^"]{2,}/],
  ["reviewThemes", /positiveReviewThemes:\s*"[^"]{5,}/],
];

const clubs = [];
for (const b of blocks) {
  const id = parseInt(b, 10);
  const name = (b.match(/name:\s*"([^"]+)"/) || [])[1];
  const city = (b.match(/city:\s*"([^"]+)"/) || [])[1];
  const state = (b.match(/state:\s*"([A-Z]{2})"/) || [])[1];
  const courts = parseInt((b.match(/numberOfCourts:\s*(\d+)/) || [])[1] || "0", 10);
  const reviews = parseInt((b.match(/reviewCount:\s*(\d+)/) || [])[1] || "0", 10);
  if (!name) continue;
  const missing = FIELDS.filter(([, re]) => !re.test(b)).map(([f]) => f);
  clubs.push({ id, name, city, state, courts, reviews, missing });
}

const totals = {};
for (const [f] of FIELDS) totals[f] = 0;
for (const c of clubs) for (const f of c.missing) totals[f]++;

// Priority: big clubs (courts) and well-reviewed clubs with the most gaps first
const prioritized = [...clubs]
  .filter((c) => c.missing.length > 0)
  .sort((a, b) => b.courts + b.reviews / 50 - (a.courts + a.reviews / 50) || b.missing.length - a.missing.length);

let md = `# Club Data Enrichment Report\n\nGenerated by \`scripts/audit-club-data.js\`. Re-run any time: \`node scripts/audit-club-data.js\`\n\n`;
md += `**${clubs.length} clubs audited.** Field coverage gaps:\n\n| Field | Clubs missing it | Coverage |\n|---|---|---|\n`;
for (const [f] of FIELDS) {
  const miss = totals[f];
  md += `| ${f} | ${miss} | ${Math.round(((clubs.length - miss) / clubs.length) * 100)}% |\n`;
}

md += `\n## Priority enrichment queue\n\nBiggest clubs (by courts + review volume) with the most missing data — fixing these first has the most SEO and user impact.\n\n| Club | Location | Courts | Reviews | Missing |\n|---|---|---|---|---|\n`;
for (const c of prioritized.slice(0, 40)) {
  md += `| ${c.name} (id ${c.id}) | ${c.city}, ${c.state} | ${c.courts || "?"} | ${c.reviews} | ${c.missing.join(", ")} |\n`;
}

md += `\n## Full gap counts by club\n\n<details><summary>All ${prioritized.length} clubs with gaps</summary>\n\n`;
for (const c of prioritized) {
  md += `- **${c.name}** (${c.city}, ${c.state}) — missing: ${c.missing.join(", ")}\n`;
}
md += `\n</details>\n`;

fs.writeFileSync(OUT_FILE, md);
console.log(`Audited ${clubs.length} clubs → ${OUT_FILE}`);
console.log("Field coverage:");
for (const [f] of FIELDS) {
  console.log(`  ${f}: ${clubs.length - totals[f]}/${clubs.length}`);
}
